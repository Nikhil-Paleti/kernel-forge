"""
https://leetgpu.com/challenges/sigmoid-linear-unit
"""

import torch
import triton
import triton.language as tl

@triton.jit
def silu_kernel(input, output, n_elements, BLOCK_SIZE: tl.constexpr):
    pid = tl.program_id(0)
    offs = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    mask = offs < n_elements
    x = tl.load(input + offs, mask=mask)
    y = x * tl.sigmoid(x) 
    tl.store(output + offs, y, mask=mask)
    

# input, output are tensors on the GPU
def solve(input: torch.Tensor, output: torch.Tensor, N: int):
    BLOCK_SIZE = 1024
    grid = (triton.cdiv(N, BLOCK_SIZE),)
    silu_kernel[grid](input, output, N, BLOCK_SIZE)
